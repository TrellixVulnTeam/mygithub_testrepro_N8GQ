def fib(max):
    n,a,b=0,0,1
    while n<max:

        yield b
        a,b=b,a+b
        n=n+1

    return 'done'

f=fib(10)
print(f.__next__())

g=fib(100)
while True:
    try:
        x=next(g)
        print('g:',x)
    except StopIteration as e:
        print("生成器返回值是：",e.value)
        break



